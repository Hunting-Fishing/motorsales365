export function getModelYears(category: VehicleCategory): ModelYearMap {
  return category === "motorcycle" ? MOTORCYCLE_MODEL_YEARS : CAR_MODEL_YEARS;
}

/** A model is considered available for a given year if either:
 *  (a) we have no data for it (default visible — never hide unmapped models), or
 *  (b) its YearRange contains the year. */
export function modelMatchesYear(
  category: VehicleCategory,
  make: string,
  model: string,
  year: number | undefined,
): boolean {
  if (!year) return true;
  const map = getModelYears(category);
  const makeKey = Object.keys(map).find((k) => norm(k) === norm(make));
  if (!makeKey) return true;
  const modelKey = Object.keys(map[makeKey]).find((k) => norm(k) === norm(model));
  if (!modelKey) return true;
  const r = map[makeKey][modelKey];
  if (year < r.start) return false;
  if (r.end !== undefined && year > r.end) return false;
  return true;
}

